int main()
{
    int n;cin>>n;
    double s1=0,s2=0;
    if(n<=50){
        s1=n*1.728;
        s2=n*1.728;
    }
    else if(n<=100) {
        s1=1.728*50+(n-50)*1.786;
        s2=n*1.728;
    }
    else if(n<=200){
        s1=1.728*50+(50)*1.786+(n-100)*2.074;
        s2=100*1.728+(n-100)*2.074;
    }
    else if(n<=300){
        s1=1.728*50+(50)*1.786+100*2.074+(n-200)*2.612;
        s2=100*1.728+100*2.074+(n-200)*2.612;
    }
    else if(n<=400){
        s1=1.728*50+(50)*1.786+100*2.074+(100)*2.612+(n-300)*2.919;
        s2=100*1.728+100*2.074+(n-200)*2.612;
    }
    else if(n<=700){
        s1=1.728*50+(50)*1.786+100*2.074+(100)*2.612+ 100*2.919+(n-400)*3.015;
        s2=100*1.728+100*2.074+200*2.612+(n-400)*3.111;
    }
    else {
        s1=1.728*50+(50)*1.786+100*2.074+(100)*2.612+ 100*2.919+(n-400)*3.015;
        s2=100*1.728+100*2.074+200*2.612+300*3.111+(n-700)*3.457;
    }
    printf("%.2lf",1.1*(s2-s1)*1000);
    return 0;
}
